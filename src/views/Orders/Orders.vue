                  size="small"
                  >{{ item.paymentStatus }}</el-tag
                ></span
              >
            </div>
            <div class="card-row">
              <span class="card-label">Giao hàng</span>
              <span class="card-value"
                ><el-tag
                  :type="getFulfillmentStatusType(item.fulfillmentStatus)"
                  effect="light"
                  size="small"
                  >{{ item.fulfillmentStatus }}</el-tag
                ></span
              >
            </div>
          </div>
